def lcg(seed):
    # Example LCG algorithm
    # a, c, m are constants
    a = 1103515245
    c = 12345
    m = 2**31
    return (a * seed + c) % m

def find_seed(known_number, max_iterations=100000000000000):
    for seed in range(max_iterations):
        if lcg(seed) == known_number:
            return seed
    return None

# Example usage
known_number = 1526261789 # The number you know was generated by rand()
seed = find_seed(known_number)
if seed is not None:
    print(f"Found seed: {seed}")
else:
    print("Seed not found within the given iterations.")
